<h3 class="h3">Модный бизнес и эффект мыльного пузыря</h3>
Но есть и обратная сторона роста популярности крафтового пива в России. Замечательные истории таких суперзвезд крафтового пивоварения, как Сэм Каладжионе (Dogfish Head Brewery), Миккель Бйорг Бйонсо (Mikkeller), Джеймс Уотт и Мартин Дики (Brew Dog) и многих других, вдохновляют пивоваров во всем мире, и Россия — не исключение. Однако раскрученная история состоявшегося успешного бизнеса — это только вершина айсберга, если под айсбергом подразумевать историю этих пивоварен. Тем не менее, игнорируя трудности, все спешат примерить на себя образ пивного революционера и крафтового пивовара. В США этот «крафтовый бум» настолько огромен, что об открытии собственной пивоварни говорят, как об очередной поездке на море:
