<h3 class="h3">Что происходит на рынке?</h3>
Тем временем, независимо от состояния терминологии, предложение «крафтового пива» на рынке растет. Если почти год назад AF Brew была первой пивоварней, сварившей полноценный India Pale Ale (IPA) и American Pale Ale (APA), то сегодня таких сортов разной степени «крафтовости» в России можно насчитать около десятка.

Спрос на крафтовое пиво тоже растет. Потихоньку становятся востребованными нестандартные сорта. Если пару лет назад верхом пивной незаурядности было что-то из области вишневого пива, то сейчас уделяется внимание хмелевым сортам, сортам с оригинальным набором злаков (овсяные, ржаные и прочие), сильноохмеленным и горьким сортам. Более того, интерес к такого рода оригинальному пиву наблюдается как у маленьких, так и у больших игроков рынка.

